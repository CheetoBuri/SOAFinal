/**
 * Get SVG icon by name
 * @param {string} name - Icon name from icons object
 * @param {object} options - Optional styling {width, height, color, class}
 * @returns {string} SVG HTML string
 */
export function getIcon(name, options = {}) {
    const svg = icons[name] || icons.coffee; // Default fallback
    
    if (!options.width && !options.height && !options.color && !options.class) {
        return svg;
    }
    
    // Parse and modify SVG attributes
    let modifiedSvg = svg;
    
    if (options.width) {
        modifiedSvg = modifiedSvg.replace(/width="\d+"/, `width="${options.width}"`);
    }
    if (options.height) {
        modifiedSvg = modifiedSvg.replace(/height="\d+"/, `height="${options.height}"`);
    }
    if (options.color) {
        modifiedSvg = modifiedSvg.replace(/stroke="currentColor"/, `stroke="${options.color}"`);
    }
    if (options.class) {
        modifiedSvg = modifiedSvg.replace(/<svg/, `<svg class="${options.class}"`);
    }
    
    return modifiedSvg;
}

/**
 * Create inline SVG element for specific icons used in reviews
 */
export function createReviewIcon(type) {
    const iconMap = {
        'size': 'ruler',
        'hot': 'hot',
        'cold': 'cold',
        'milk': 'milk',
        'toppings': 'sparkles',
        'sugar': 'candy'
    };
    
    return getIcon(iconMap[type] || 'coffee', { width: 14, height: 14 });
}
